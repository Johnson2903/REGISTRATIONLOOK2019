                <cenetr>About LOOK 2019</cenetr>
                <p>The theme of LOOK 2019 is ‘For this purpose’ with emphasis on Ps 40:7.
                A popular saying states that the beginning of recovery is discovery. 
                While some often see Christian missions as a burden, but God designs
                His call to be a blessing to those He calls, as He did to Abraham (Gen 12: 1-2).
                 However, lack of understanding of the attached blessings to the call often deprives
                 a lot of Christians of their earthly and eternal blessings. Thus, there is a need
                 for Christians to discover themselves in the will of God for them to be effective
                  in the course of Christian missions. </p>
                <p>LOOK 2019 will be opened with JE-HOSH-A-PHAT Mission Concert on
                    Thursday August 15 (Day 1) by 5: 00 pm. The concert shall involve the launch 
                    of 2019 musical album project of FSM and presentation of the winners of
                    Christian Music Missions Ministers’ Award. Morning session of Day 2 
                    (Friday, August 16) will feature a lot of Training of Trainers events
                     on the project arms of FSM till evening. Bro. ‘Dotun Adeboye, the National
                     Director of Christian Missionary Foundation shall take over the stage in the
                     later part of the event to minister on the theme of the conference.
                <p>Day 3 (Saturday, August 17) shall commence with the second aspect of the</p>
                    ministration by Bro. ‘Dotun Adeboye till around 10; 00 am. Thereafter, 
                    Glorious Future (a time to tap grace from testimonies of seasoned Christians
                    that God has helped to successfully passed through challenging times)
                    shall take place with a colloquium involving Prof. and Pastor (Mrs)
                    Okunlola and Prof. Sunday Adigbo. After this the annual Re-union/ Thanksgiving 
                    of students and non-student members of FSM shall take place till 2:00 pm
                     leading into Re-union lunch. Bible Study shall take place from 3: 30 – 5: 00 pm.
                 The Annual leadership Congress shall run from 6:00 – 8: 30 pm.</p>
               <p>As usual, the last day of LOOK 2019 (Sunday, August 18) shall be an
                    opportunity for hands-on training on mobilization for missions among
                     students till 11:30 am: Participants in groups shall visit Christian 
                     fellowships in Ibadan and Oyo towns. The event shall be brought to a 
                     close by 12:00 noon. 
              <p>Meanwhile, the children component of LOOK 2019 shall run concurrently from
                   Friday of the event till Saturday. The children in two categories (Primary 
                   and Secondary schools) shall be tasked on developing themselves in the Lord
                    and skills for an excellent future. </p>
               <p>By God’s grace, Medical and Spiritual Clinics shall take place all 
                   through the event, as well as social welfare at a certain period where 
                   interested brethren (especially students) shall have access to clothing
                    and other useful materials donated by other members. Members are therefore
                     implored to donate well washed fairly used or new clothing and other useful
                      items for students.    </p>
               <p>Although participation in LOOK 2019 is free of charge and no offering is 
                   collected during the event, every participant is encouraged to be part
                    of resource mobilization for the event. The unit cost per participant is
                     N6,750 (covering accommodation, feeding and other logistics of the event, 
                     excluding transportation of participants to the venue). 
                    Willful donations should be paid to account details below or materials through State chapters:</p>
